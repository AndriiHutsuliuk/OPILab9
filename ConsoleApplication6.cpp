#include <iostream>
#include <iomanip>
#include <windows.h>
#include <math.h>

using namespace std;
int main()
{
	float x, y,y1;
	cout << "Input x:";
	cin >> x;
	y1 = 0;
	for (int i = 1; i < 10; i++) {
		y1+=log(x * i);
	}
	y = cos(x) + y1 / pow(x, 2) + 2;
	cout << "Y=" << setprecision(3) << y << endl;
}


